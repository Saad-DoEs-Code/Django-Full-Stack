####### NUMBERS #######

# Dynamically Typed => No need to define datatype
num_1 = 5
num_2 = 4

# Arithmetic Operations
print(f"{num_1} + {num_2} = ",num_1 + num_2)  # Output: 9
print(f"{num_1} - {num_2} = ",num_1 - num_2)  # Output: 1
print(f"{num_1} * {num_2} = ",num_1 * num_2)  # Output: 20
print(f"{num_1} / {num_2} = ",num_1 / num_2)  # Output: 1
print(f"{num_1} ^ {num_2} = ",num_1 ** num_2)  # Output: 625
print(f"{num_1} % {num_2} = ",num_1 // num_2)  # Output: 625


# Order of Operations
print(((2+2)*3)-2)

# Type Conversion
my_income = 100
tax_rate = 0.25
tax = my_income*tax_rate
print(f"Tax: ${tax:.2f}")  # Output: Tax: $25. # Integer x Float = Float

